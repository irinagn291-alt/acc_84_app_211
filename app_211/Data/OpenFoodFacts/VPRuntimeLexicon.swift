enum VPScannedCodeNormalizer {
    static func canonicalProductCode(_ raw: String) -> String? {
        let s = raw.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !s.isEmpty else { return nil }

        if let fromPath = firstCapture(pattern: VPRuntimeLexicon.regexProductPath, in: s) {
            return fromPath
        }
        if let fromQuery = firstCapture(pattern: VPRuntimeLexicon.regexQueryParams, in: s) {
            return fromQuery
        }

        let longestRun = longestDigitRun(in: s)
        if let longestRun, (8 ... 14).contains(longestRun.count) {
            return longestRun
        }

        let only = s.filter(\.isNumber)
        if (8 ... 14).contains(only.count) { return only }
        if only.count > 14, let longestRun { return String(longestRun.prefix(14)) }
        if only.count > 14 { return String(only.prefix(14)) }
        return longestRun
    }

    private static func firstCapture(pattern: String, in haystack: String) -> String? {
        guard let regex = try? NSRegularExpression(pattern: pattern, options: []) else { return nil }
        let range = NSRange(haystack.startIndex ..< haystack.endIndex, in: haystack)
        guard let match = regex.firstMatch(in: haystack, options: [], range: range), match.numberOfRanges > 1 else {
            return nil
        }
        let g = match.range(at: 1)
        guard let swiftRange = Range(g, in: haystack) else { return nil }
        return String(haystack[swiftRange])
    }

    private static func longestDigitRun(in s: String) -> String? {
        var best = ""
        var current = ""
        for ch in s {
            if ch.isNumber {
                current.append(ch)
            } else {
                if current.count > best.count { best = current }
                current = ""
            }
        }
        if current.count > best.count { best = current }
        return best.isEmpty ? nil : best
    }
}

